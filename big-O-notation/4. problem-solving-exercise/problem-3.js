// টাইম কমপ্লেক্সিটি বিশ্লেষণ:
// এই ফাংশনটি একটি অ্যারের প্রথম উপাদান রিটার্ন করছে, যেটা arr[0]। এখানে আমরা অ্যারের প্রথম
// ইনডেক্সে যাচ্ছি এবং সেই উপাদানটি রিটার্ন করছি।
// - অ্যারেতে প্রথম ইনডেক্সে অ্যাক্সেস করা একটি constant time অপারেশন, অর্থাৎ এটা কোনো লুপ
//   বা পুনরাবৃত্তি ছাড়াই সরাসরি কাজ করে।
// - প্রতিটি অ্যারের ইনডেক্সে সরাসরি অ্যাক্সেস করার সময় লাগবে O(1), অর্থাৎ constant time।

// টাইম কমপ্লেক্সিটি:
// - এই ফাংশনের টাইম কমপ্লেক্সিটি হলো O(1)। কারণ এটি একটি নির্দিষ্ট স্থানে (প্রথম ইনডেক্সে) সরাসরি যায়
// এবং কোনো অতিরিক্ত সময় লাগে না ইনপুট অ্যারের আকার বড় বা ছোট হওয়ার উপর ভিত্তি করে।

// উদাহরণ:
// যদি ইনপুট অ্যারে [10, 20, 30, 40] হয়, তাহলে arr[0]-এর মাধ্যমে সরাসরি ১০ রিটার্ন করবে।
// অ্যারের আকার বড় হলেও, এটি প্রথম উপাদানে দ্রুতই পৌঁছবে, তাই সময়ের হিসাব অনুযায়ী এটা
// O(1) টাইম কমপ্লেক্সিটিতে কাজ করবে।

// ব্যাখ্যা:
// বাংলায় বলতে গেলে, এই ফাংশনটি অ্যারের প্রথম উপাদানটি খুব দ্রুত রিটার্ন করে। এটার জন্য কোনো
// অতিরিক্ত কাজ করতে হয় না, তাই আমরা এটাকে constant time বা O(1) টাইম কমপ্লেক্সিটি বলি।
