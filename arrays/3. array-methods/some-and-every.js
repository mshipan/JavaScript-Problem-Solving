// some() এবং every() হলো জাভাস্ক্রিপ্টের দুটি ইনবিল্ট মেথড, যেগুলো অ্যারের উপাদানগুলো নির্দিষ্ট শর্ত
// পূরণ করে কিনা তা যাচাই করতে ব্যবহৃত হয়। এরা উভয়ই বুলিয়ান (true/false) মান রিটার্ন করে।

// --------------------------------------------------------------------------

// ১. some() মেথড
// some() মেথডটি একটি অ্যারের অন্তত একটি উপাদান নির্দিষ্ট শর্ত পূরণ করছে কিনা তা যাচাই করে। যদি
// কোনো উপাদান শর্ত পূরণ করে, তাহলে এটি true রিটার্ন করে। যদি কোনো উপাদান শর্ত পূরণ না করে,
// তাহলে এটি false রিটার্ন করে।

// সিনট্যাক্স:
array.some(function (element, index, array) {
  // শর্ত
});

// - element: অ্যারের বর্তমান উপাদান।
// - index: বর্তমান উপাদানের ইনডেক্স (ঐচ্ছিক)।
// - array: অ্যারেটি নিজেই (ঐচ্ছিক)।

// --------------------------------------------------------------------------

// উদাহরণ:
let numbers = [1, 2, 3, 4, 5];

let hasEvenNumber = numbers.some(function (number) {
  return number % 2 === 0;
});

console.log(hasEvenNumber); // আউটপুট: true (কারণ ২ এবং ৪ জোড় সংখ্যা)

// এখানে some() মেথডটি যাচাই করেছে যে অ্যারেতে অন্তত একটি জোড় সংখ্যা আছে কিনা।

// --------------------------------------------------------------------------

// ২. every() মেথড
// every() মেথডটি একটি অ্যারের সবগুলো উপাদান নির্দিষ্ট শর্ত পূরণ করছে কিনা তা যাচাই করে। যদি সবগুলো উপাদান শর্ত পূরণ করে, তাহলে এটি true রিটার্ন করে। যদি কোনো একটি উপাদান শর্ত পূরণ না করে, তাহলে এটি false রিটার্ন করে।

// সিনট্যাক্স:
array.every(function (element, index, array) {
  // শর্ত
});

// - element: অ্যারের বর্তমান উপাদান।
// - index: বর্তমান উপাদানের ইনডেক্স (ঐচ্ছিক)।
// - array: অ্যারেটি নিজেই (ঐচ্ছিক)।

// --------------------------------------------------------------------------

// উদাহরণ:
let numbers2 = [2, 4, 6, 8];

let allEven = numbers2.every(function (number) {
  return number % 2 === 0;
});

console.log(allEven); // আউটপুট: true (কারণ সব সংখ্যা জোড়)

// এখানে every() মেথডটি যাচাই করেছে যে অ্যারের সব উপাদান জোড় সংখ্যা কিনা।

// --------------------------------------------------------------------------

// মূল পার্থক্য:
// 1. some() মেথড: অ্যারের অন্তত একটি উপাদান শর্ত পূরণ করলে true রিটার্ন করে।
// 2. every() মেথড: অ্যারের সব উপাদান শর্ত পূরণ করলে true রিটার্ন করে।

// --------------------------------------------------------------------------

// আরো উদাহরণ:
let ages = [18, 20, 25, 30];

// `some()` দিয়ে চেক করা যদি অন্তত একজন মাইনর থাকে (১৮ বছরের নিচে)
let hasMinor = ages.some(function (age) {
  return age < 18;
});
console.log(hasMinor); // আউটপুট: false (কারণ কেউ ১৮ বছরের নিচে নেই)

// `every()` দিয়ে চেক করা যদি সবাই অ্যাডাল্ট হয় (১৮ বছরের বেশি)
let allAdults = ages.every(function (age) {
  return age >= 18;
});
console.log(allAdults); // আউটপুট: true (কারণ সবাই ১৮ বছরের বেশি)
