// fill() মেথডের ব্যাখ্যা
// fill() হলো জাভাস্ক্রিপ্টের একটি মেথড যা কোনো অ্যারের সব উপাদানকে একটি নির্দিষ্ট মানে পরিবর্তন করতে ব্যবহৃত হয়। 
// এটি মূল অ্যারের উপরেই কাজ করে এবং অ্যারের নতুন অবস্থাটিকে রিটার্ন করে।

// --------------------------------------------------------------------------

// fill() মেথডের বৈশিষ্ট্য:
// - এটি মূল অ্যারের সকল উপাদানকে একটি নির্দিষ্ট মানে পূর্ণ করে।
// - start এবং end ইনডেক্স দেওয়ার মাধ্যমে নির্দিষ্ট পরিসরে উপাদান পরিবর্তন করা যায়।
// - এটি মূল অ্যারেটির উপরেই পরিবর্তন আনে (in-place) এবং পরিবর্তিত অ্যারেটি রিটার্ন করে।

// --------------------------------------------------------------------------

// সিনট্যাক্স:
array.fill(value, start, end)

// - value: যে মানটি দিয়ে অ্যারের উপাদান পূর্ণ করা হবে।
// - start: যে ইনডেক্স থেকে পূরণ শুরু হবে (ডিফল্ট 0)।
// - end: যে ইনডেক্সে পূরণ শেষ হবে (ডিফল্ট অ্যারের দৈর্ঘ্য)।

// --------------------------------------------------------------------------

// উদাহরণ: পুরো অ্যারে পূর্ণ করা
let numbers = [1, 2, 3, 4, 5];
numbers.fill(0);

console.log(numbers); // আউটপুট: [0, 0, 0, 0, 0]

// এখানে fill(0) মেথডটি পুরো numbers অ্যারেকে 0 মানে পূর্ণ করেছে।

// --------------------------------------------------------------------------

// নির্দিষ্ট পরিসরে পূরণ করা
let numbers2 = [1, 2, 3, 4, 5];
numbers2.fill(7, 1, 4);

console.log(numbers2); // আউটপুট: [1, 7, 7, 7, 5]

// এখানে, fill(7, 1, 4) মেথডটি অ্যারের 1 ইনডেক্স থেকে 4 ইনডেক্সের আগে পর্যন্ত উপাদানগুলোকে 7 মানে পূর্ণ করেছে।

// --------------------------------------------------------------------------

// শূন্য দৈর্ঘ্যের অ্যারে তৈরি ও পূরণ করা
let emptyArray = new Array(5).fill(1);

console.log(emptyArray); // আউটপুট: [1, 1, 1, 1, 1]

// এখানে, নতুন একটি ৫ দৈর্ঘ্যের শূন্য অ্যারে তৈরি করে fill(1) মেথড দিয়ে প্রত্যেকটি উপাদানকে 1 মানে পূর্ণ করা হয়েছে।

// --------------------------------------------------------------------------

// fill() মেথডের সুবিধা:
// 1. সহজে অ্যারে পূরণ করা: নির্দিষ্ট মান দিয়ে দ্রুত অ্যারে পূরণ করা যায়।
// 2. নির্দিষ্ট পরিসর পূরণ: কোনো নির্দিষ্ট পরিসরে উপাদানগুলো পরিবর্তন করার সুযোগ দেয়।

// --------------------------------------------------------------------------

// সতর্কতা:
// যেহেতু fill() মেথডটি মূল অ্যারের উপরেই পরিবর্তন আনে, তাই এটি অ্যারের কপি তৈরির পর ব্যবহৃত হলে মূল অ্যারে অপরিবর্তিত থাকে।
let originalArray = [1, 2, 3, 4, 5];
let filledArray = [...originalArray].fill(8, 2, 4);

console.log(originalArray); // আউটপুট: [1, 2, 3, 4, 5]
console.log(filledArray);   // আউটপুট: [1, 2, 8, 8, 5]

// এখানে originalArray অপরিবর্তিত থাকে, আর filledArray-এর নির্দিষ্ট পরিসরে মান পরিবর্তিত হয়।
