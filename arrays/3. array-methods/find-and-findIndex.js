// শর্ত অনুযায়ী অনুসন্ধান করতে ব্যবহৃত হয়। এই মেথডগুলো শর্ত পূরণকারী প্রথম উপাদানটিকে খুঁজে বের
// করে।

// --------------------------------------------------------------------------

// ১. find() মেথড
// find() মেথডটি একটি অ্যারের মধ্যে থেকে প্রথম উপাদানটিকে রিটার্ন করে, যা নির্দিষ্ট শর্ত পূরণ করে। যদি
// কোনো উপাদান শর্ত পূরণ না করে, তাহলে এটি undefined রিটার্ন করে।

// সিনট্যাক্স:
array.find(function (element, index, array) {
  // শর্ত
});

// - element: অ্যারের বর্তমান উপাদান।
// - index: বর্তমান উপাদানের ইনডেক্স (ঐচ্ছিক)।
// - array: অ্যারেটি নিজেই (ঐচ্ছিক)।

// --------------------------------------------------------------------------

// উদাহরণ:
let numbers = [5, 12, 8, 130, 44];

let found = numbers.find(function (number) {
  return number > 10;
});

console.log(found); // আউটপুট: 12 (কারণ ১২ শর্ত পূরণ করা প্রথম সংখ্যা)

// এখানে find() মেথডটি অ্যারের প্রথম উপাদান খুঁজে বের করেছে যা ১০-এর চেয়ে বড়।

// --------------------------------------------------------------------------

// ২. findIndex() মেথড
// findIndex() মেথডটি একটি অ্যারের মধ্যে প্রথম উপাদানের ইনডেক্স রিটার্ন করে, যা নির্দিষ্ট শর্ত পূরণ
// করে। যদি কোনো উপাদান শর্ত পূরণ না করে, তাহলে এটি -1 রিটার্ন করে।

// সিনট্যাক্স:
array.findIndex(function (element, index, array) {
  // শর্ত
});

// - element: অ্যারের বর্তমান উপাদান।
// - index: বর্তমান উপাদানের ইনডেক্স (ঐচ্ছিক)।
// - array: অ্যারেটি নিজেই (ঐচ্ছিক)।

// --------------------------------------------------------------------------

// উদাহরণ:
let numbers2 = [5, 12, 8, 130, 44];

let foundIndex = numbers2.findIndex(function (number) {
  return number > 10;
});

console.log(foundIndex); // আউটপুট: 1 (কারণ ১২ শর্ত পূরণ করা প্রথম উপাদান এবং তার
// ইনডেক্স ১)

// এখানে findIndex() মেথডটি অ্যারের প্রথম উপাদানের ইনডেক্স রিটার্ন করেছে যা ১০-এর চেয়ে বড়।

// --------------------------------------------------------------------------

// মূল পার্থক্য:
// 1. find(): শর্ত পূরণকারী প্রথম উপাদান রিটার্ন করে।
// 2. findIndex(): শর্ত পূরণকারী প্রথম উপাদানের ইনডেক্স রিটার্ন করে। যদি না পায়, তাহলে -1 রিটার্ন
//    করে।

// --------------------------------------------------------------------------

// আরো উদাহরণ:
let students = [
  { name: "Shipan", age: 25 },
  { name: "Rahim", age: 18 },
  { name: "Karim", age: 17 },
];

// `find()` দিয়ে প্রথম অ্যাডাল্ট (১৮ বছরের বেশি) খুঁজে বের করা
let adult = students.find(function (student) {
  return student.age >= 18;
});
console.log(adult); // আউটপুট: { name: "Shipan", age: 25 }

// `findIndex()` দিয়ে প্রথম অ্যাডাল্টের ইনডেক্স খুঁজে বের করা
let adultIndex = students.findIndex(function (student) {
  return student.age >= 18;
});
console.log(adultIndex); // আউটপুট: 0 (শিপানের ইনডেক্স ০)
