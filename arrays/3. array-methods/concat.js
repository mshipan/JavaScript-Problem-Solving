// concat() হলো জাভাস্ক্রিপ্টের একটি মেথড যা দুটি বা তার বেশি অ্যারে একসাথে সংযুক্ত (concatenate)
// করে এবং একটি নতুন অ্যারে রিটার্ন করে। এটি মূল অ্যারেগুলোর কোনো পরিবর্তন করে না; বরং নতুন
// একটি অ্যারে তৈরি করে, যেখানে আগের সব অ্যারেগুলোর উপাদান যুক্ত থাকে।

// --------------------------------------------------------------------------

// concat() মেথডের বৈশিষ্ট্য:
// এটি কোনো বিদ্যমান অ্যারেকে পরিবর্তন করে না, বরং নতুন একটি অ্যারে রিটার্ন করে।
// এটি একাধিক অ্যারে অথবা একাধিক মান যুক্ত করতে সক্ষম।

// সিনট্যাক্স:
// array1.concat(array2, array3, ..., arrayN)

// - array1: প্রথম অ্যারে, যেটির সাথে অন্যান্য অ্যারে যুক্ত হবে।
// - array2, array3, ..., arrayN: এক বা একাধিক অ্যারে বা মান, যা আপনি প্রথম অ্যারের সাথে
//   যুক্ত করতে চান।

// --------------------------------------------------------------------------

// উদাহরণ:
let array1 = [1, 2, 3];
let array2 = [4, 5, 6];

// দুইটি অ্যারে সংযুক্ত করা
let combinedArray = array1.concat(array2);

console.log(combinedArray); // আউটপুট: [1, 2, 3, 4, 5, 6]

// এখানে, array1 এবং array2 কে concat() মেথড দিয়ে সংযুক্ত করা হয়েছে এবং এর ফলে একটি নতুন
// অ্যারে তৈরি হয়েছে, যেখানে উভয় অ্যারের উপাদান যুক্ত হয়েছে।

// --------------------------------------------------------------------------

// অনেকগুলো অ্যারে সংযুক্ত করার উদাহরণ:
let array3 = ["apple", "banana"];
let array4 = ["mango", "orange"];
let array5 = ["grape", "pineapple"];

// তিনটি অ্যারে একসাথে সংযুক্ত করা
let allFruits = array3.concat(array4, array5);

console.log(allFruits); // আউটপুট: ["apple", "banana", "mango", "orange", "grape",
// "pineapple"]

// এখানে, তিনটি আলাদা আলাদা অ্যারে concat() দিয়ে সংযুক্ত করা হয়েছে এবং সব ফল একসাথে একটি
// নতুন অ্যারেতে পাওয়া গেছে।

// --------------------------------------------------------------------------

// একটি অ্যারের সাথে একক মান যুক্ত করার উদাহরণ:
let numbers = [1, 2, 3];

// একটি অ্যারের সাথে একটি একক মান যুক্ত করা
let updatedNumbers = numbers.concat(4);

console.log(updatedNumbers); // আউটপুট: [1, 2, 3, 4]

// এখানে, concat() মেথড ব্যবহার করে একটি অ্যারের সাথে একটি একক সংখ্যা 4 যুক্ত করা হয়েছে এবং
// এটি একটি নতুন অ্যারে রিটার্ন করেছে।

// --------------------------------------------------------------------------

// concat() মেথডের উপকারিতা:
// 1. অপরিবর্তনশীল (Immutable): এটি মূল অ্যারেগুলো পরিবর্তন করে না, বরং নতুন একটি অ্যারে তৈরি
//    করে।
// 2. একাধিক অ্যারে সংযুক্ত করা: একাধিক অ্যারে সংযুক্ত করার জন্য এটি একটি সহজ পদ্ধতি।
// 3. একক মান যুক্ত করা: শুধুমাত্র অ্যারে নয়, একক মানও অ্যারেতে যুক্ত করা সম্ভব।
