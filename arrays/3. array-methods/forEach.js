// forEach() হলো জাভাস্ক্রিপ্টের একটি ইনবিল্ট মেথড, যা অ্যারের প্রতিটি উপাদানের উপর একটি ফাংশন
// চালায়। প্রতিটি উপাদানের জন্য আলাদাভাবে একটি নির্দিষ্ট কাজ বা অপারেশন করার জন্য এটি ব্যবহার করা
// হয়।

// এটি অ্যারের প্রত্যেকটি উপাদানকে ধরে ধরে আপনার প্রদত্ত ফাংশনটি চালাবে, কিন্তু কোনো ভ্যালু রিটার্ন করবে
// না। মূলত, এটি কোনো নতুন অ্যারে তৈরি করে না বা অ্যারের অবস্থা পরিবর্তন করে না, বরং একটি
// নির্দিষ্ট কাজ সম্পাদন করতে ব্যবহৃত হয়।

// --------------------------------------------------------------------------

// forEach() এর সিনট্যাক্স:
array.forEach(function (element, index, array) {
  // এখানে আপনি যা করতে চান
});

// - element: বর্তমান উপাদান।
// - index: ঐ উপাদানের ইনডেক্স নম্বর।
// - array: মূল অ্যারেটি।

// --------------------------------------------------------------------------

// উদাহরণ:

let numbers = [1, 2, 3, 4, 5];

numbers.forEach(function (number) {
  console.log(number * 2);
});

// আউটপুট:
// 2
// 4
// 6
// 8
// 10

// এখানে, forEach() মেথডটি numbers অ্যারের প্রতিটি উপাদানে কাজ করে এবং প্রতিটি উপাদানকে ২
// দিয়ে গুণ করে কনসোলে আউটপুট দেখায়।

// --------------------------------------------------------------------------

// কিছু পয়েন্ট:
// 1. forEach() ভ্যালু রিটার্ন করে না: এটি কোনো নতুন অ্যারে তৈরি করে না বা ভ্যালু রিটার্ন করে না,
//    শুধুমাত্র অ্যারের প্রতিটি উপাদানে কাজ করে।

// 2. ফাংশনটি অ্যারের প্রতিটি উপাদানের জন্য চালানো হয়: প্রতিটি উপাদানের জন্য আপনি যে ফাংশনটি
//    দিবেন সেটি একবার করে চালানো হবে।

// 3. forEach() ইনডেক্স বা অ্যারে সম্পর্কে তথ্য সরবরাহ করতে পারে: আপনি যদি চান, তাহলে index
//    বা array প্যারামিটার ব্যবহার করে আপনি ইনডেক্স বা পুরো অ্যারের তথ্যও পেতে পারেন।

// --------------------------------------------------------------------------

// উদাহরণ (ইনডেক্স সহ):

let names = ["Shipan", "Mallik", "John"];

names.forEach(function (name, index) {
  console.log(index + ": " + name);
});

// আউটপুট:
// 0: Shipan
// 1: Mallik
// 2: John

// এখানে forEach() মেথডটি names অ্যারের প্রতিটি উপাদানের ইনডেক্স নম্বরসহ উপাদানের নাম
// প্রিন্ট করেছে।
