// keys(), values(), এবং entries() মেথডগুলো জাভাস্ক্রিপ্ট অ্যারের উপর বিভিন্ন ধরণের ডাটা অ্যাক্সেসের জন্য
// ব্যবহার করা হয়। এগুলো ব্যবহার করে একটি অ্যারের ইন্ডেক্স, মান, এবং ইন্ডেক্স-মূল্যের জোড়া পাওয়া যায়।
// নিচে প্রতিটি মেথডের কাজ বিস্তারিতভাবে ব্যাখ্যা করা হলো:

// --------------------------------------------------------------------------

// ১. keys() মেথড
// keys() মেথডটি অ্যারের সমস্ত ইন্ডেক্সগুলোকে একটি ইটারেটর (iterator) হিসাবে প্রদান করে।
let numbers = [10, 20, 30];
let keys = numbers.keys();

for (let key of keys) {
  console.log(key); // আউটপুট: 0, 1, 2
}

// এখানে keys() শুধুমাত্র অ্যারের ইন্ডেক্সগুলোকে প্রদান করে। উপরের উদাহরণে, আউটপুট হবে 0, 1, এবং 2, কারণ এগুলো অ্যারের ইন্ডেক্স।

// --------------------------------------------------------------------------

// ২. values() মেথড
// values() মেথডটি অ্যারের প্রতিটি মানকে একটি ইটারেটর (iterator) হিসাবে প্রদান করে। এটি শুধুমাত্র অ্যারের মানগুলোকে প্রদান করে, ইন্ডেক্স নয়।
let numbers2 = [10, 20, 30];
let values = numbers2.values();

for (let value of values) {
  console.log(value); // আউটপুট: 10, 20, 30
}

// এখানে values() শুধুমাত্র অ্যারের মানগুলো প্রদান করে, যেমন উপরের উদাহরণে আউটপুট হবে 10, 20, এবং 30।

// --------------------------------------------------------------------------

// ৩. entries() মেথড
// entries() মেথডটি অ্যারের প্রতিটি ইন্ডেক্স-মূল্যের জোড়াকে একটি ইটারেটর (iterator) হিসাবে প্রদান করে। অর্থাৎ, এটি
// প্রতিটি উপাদানের জন্য [ইন্ডেক্স, মান] এর জোড়া (pair) প্রদান করে।
let numbers3 = [10, 20, 30];
let entries = numbers3.entries();

for (let entry of entries) {
  console.log(entry); // আউটপুট: [0, 10], [1, 20], [2, 30]
}

// এখানে entries() মেথডটি প্রতিটি ইন্ডেক্স এবং মানকে একত্রে একটি জোড়া হিসাবে প্রদান করে। যেমন, [0, 10], [1, 20], এবং [2, 30]।

// --------------------------------------------------------------------------

// সংক্ষেপে:
// - keys() - শুধুমাত্র ইন্ডেক্স প্রদান করে।
// - values() - শুধুমাত্র মান প্রদান করে।
// - entries() - ইন্ডেক্স এবং মানের জোড়া প্রদান করে।
// এগুলো ব্যবহার করে অ্যারের ডাটাকে বিভিন্ন ভাবে সহজে অ্যাক্সেস এবং প্রোসেস করা যায়।
