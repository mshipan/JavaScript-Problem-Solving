// map() হলো জাভাস্ক্রিপ্টের একটি ইনবিল্ট মেথড, যা একটি অ্যারের প্রতিটি উপাদানের উপর নির্দিষ্ট কাজ
// করে এবং সেই কাজের আউটপুট হিসেবে একটি নতুন অ্যারে তৈরি করে।

// map() মেথডটি প্রতিটি উপাদানের উপর একটি নির্দিষ্ট ফাংশন চালায় এবং সেই ফাংশনের রিটার্ন ভ্যালুগুলো
// নিয়ে একটি নতুন অ্যারে তৈরি করে। এটি মূল অ্যারেটির কোনো পরিবর্তন করে না, বরং নতুন অ্যারে রিটার্ন
// করে।

// --------------------------------------------------------------------------

// map() এর সিনট্যাক্স:
let newArray = array.map(function (element, index, array) {
  // প্রতিটি উপাদানের উপর কাজ
  return /* রিটার্ন ভ্যালু */;
});

// - element: বর্তমান উপাদান।
// - index: ঐ উপাদানের ইনডেক্স নম্বর।
// - array: মূল অ্যারেটি।
// - ফাংশন: যে কাজটি আপনি প্রতিটি উপাদানের উপর করতে চান, সেটি।

// --------------------------------------------------------------------------

// উদাহরণ:
let numbers = [1, 2, 3, 4, 5];

let doubledNumbers = numbers.map(function (number) {
  return number * 2;
});

console.log(doubledNumbers);

// আউটপুট:
// [2, 4, 6, 8, 10]

// এখানে, map() মেথডটি numbers অ্যারের প্রতিটি উপাদানের উপর ২ দিয়ে গুণ করে এবং একটি নতুন
// অ্যারে doubledNumbers তৈরি করে। মূল অ্যারেটি অপরিবর্তিত থাকে।

// --------------------------------------------------------------------------

// কিছু পয়েন্ট:
// 1. map() একটি নতুন অ্যারে তৈরি করে: map() মেথডটি মূল অ্যারেকে পরিবর্তন না করে একটি নতুন
//    অ্যারে তৈরি করে যা প্রতিটি উপাদানের উপর নির্দিষ্ট কাজ সম্পন্ন করার পর রিটার্ন হয়।

// 2. প্রতিটি উপাদানে ফাংশন চালায়: প্রতিটি উপাদানের জন্য map() মেথডটি ফাংশন চালায় এবং সেই
//    ফাংশনের রিটার্ন ভ্যালুগুলো নিয়ে নতুন অ্যারে তৈরি করে।

// 3. অ্যারের আকার অপরিবর্তিত থাকে: নতুন অ্যারে এবং পুরানো অ্যারের আকার সমান হয়, অর্থাৎ প্রতিটি
//    উপাদানের জন্য একটি নতুন উপাদান থাকে।

// --------------------------------------------------------------------------

// উদাহরণ (ইনডেক্স সহ):

let numbers2 = [1, 2, 3, 4, 5];

let modifiedArray = numbers2.map(function (number, index) {
  return number * index;
});

console.log(modifiedArray);

// আউটপুট:
// [0, 2, 6, 12, 20]

// এখানে, map() প্রতিটি উপাদানকে তার ইনডেক্সের সাথে গুণ করেছে এবং নতুন অ্যারে তৈরি করেছে।
