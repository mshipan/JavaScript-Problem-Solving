// Array.isArray() মেথড
// Array.isArray() মেথডটি JavaScript এর একটি বিল্ট-ইন মেথড, যা চেক করে যে একটি নির্দিষ্ট মানটি এ্যারে কি না।
// এটি একটি boolean value রিটার্ন করে — true যদি মানটি একটি অ্যারে হয় এবং false যদি না হয়।

// --------------------------------------------------------------------------

// সিনট্যাক্স:
Array.isArray(value);

// - value: এটি সেই মান, যেটি আপনি যাচাই করতে চান যে এটি একটি অ্যারে কিনা।

// --------------------------------------------------------------------------

// উদাহরণ:
let arr = [1, 2, 3];
let obj = { name: "John", age: 30 };
let str = "hello";

// অ্যারে চেক করা
console.log(Array.isArray(arr)); // আউটপুট: true
console.log(Array.isArray(obj)); // আউটপুট: false
console.log(Array.isArray(str)); // আউটপুট: false

// আরও উদাহরণ
console.log(Array.isArray([])); // আউটপুট: true
console.log(Array.isArray([1])); // আউটপুট: true
console.log(Array.isArray({})); // আউটপুট: false

// --------------------------------------------------------------------------

// ব্যাখ্যা:
// - Array.isArray(arr) যেখানে arr একটি অ্যারে, সেই ক্ষেত্রে এটি true রিটার্ন করবে।
// - অন্যদিকে, একটি অবজেক্ট বা স্ট্রিং বা অন্য কোনো ডাটা টাইপের ক্ষেত্রে এটি false রিটার্ন করবে।

// --------------------------------------------------------------------------

// কেন ব্যবহার করবেন?
// Array.isArray() মেথডটি বিশেষভাবে সহায়ক যখন আপনি কোডে ভ্যালিডেশন করতে চান যে একটি ভ্যারিয়েবল অ্যারে কিনা।
// কখনও কখনও অ্যারে এবং অবজেক্ট এর মধ্যে বিভ্রান্তি হতে পারে, বিশেষত যখন কোন অবজেক্টকে অ্যারে হিসেবে ব্যবহার করার চেষ্টা করা হয়।

// এটি অ্যারে চেক করার জন্য একটি নির্ভুল পদ্ধতি, কারণ অন্যান্য পদ্ধতি যেমন typeof কেবল একটি সাধারণ ডাটা টাইপ রিটার্ন
// করে যা অ্যারে চেক করার জন্য যথেষ্ট নয়।
