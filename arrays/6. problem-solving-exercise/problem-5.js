// Write a function to check if a specific value exists in an array.

// Approach 1: Using for Loop
function findValue(arr, n) {
  if (arr.length === 0) return 0;
  for (let i = 0; i <= arr.length - 1; i++) {
    if (arr[i] === n) {
      // যদি উপাদানটি নির্দিষ্ট মানের সমান হয়
      return true; // মানটি অ্যারেতে পাওয়া গেছে
    }
  }
  return false; // মানটি অ্যারেতে পাওয়া যায়নি
}

const array = [1, 2, 3, 4, 5, 6, 7];
console.log(findValue(array, 7));

// ---------------------------------------------------------------------------

// ব্যাখ্যা:
// 1. প্রতিটি উপাদান চেক করার জন্য একটি লুপ ব্যবহার করা হয়েছে।
// 2. যদি কোনো উপাদান নির্দিষ্ট মানের সমান হয়, তাহলে ফাংশন true রিটার্ন করবে।
// 3. লুপ শেষে যদি কোনো মান পাওয়া না যায়, তাহলে false রিটার্ন করবে।

// ---------------------------------------------------------------------------

// Approach 2: Using includes() Method
function checkValueExists(arr, value) {
  return arr.includes(value);
}

const nums = [10, 20, 30, 40, 50];
console.log(checkValueExists(nums, 9));

// ---------------------------------------------------------------------------

// ব্যাখ্যা:
// 1. includes() মেথড সরাসরি অ্যারেতে নির্দিষ্ট মান আছে কিনা তা চেক করে।
// 2. এটি প্রথমে true বা false রিটার্ন করে।

// ---------------------------------------------------------------------------

// Edge Case: Empty Array
// যদি ইনপুট অ্যারে ফাঁকা হয়, উভয় পদ্ধতিই সরাসরি false রিটার্ন করবে কারণ কোনো উপাদান নেই চেক করার জন্য।

// ---------------------------------------------------------------------------

// Time Complexity:
// for লুপ:
// O(n), কারণ প্রতিটি উপাদান একবার করে চেক করা হয়।

// includes():
// O(n), কারণ এটি একইভাবে প্রতিটি উপাদান চেক করে।

// ---------------------------------------------------------------------------

// Space Complexity:
// উভয় পদ্ধতিতেই কোনো অতিরিক্ত স্পেস ব্যবহার করা হয় না, তাই O(1)।
