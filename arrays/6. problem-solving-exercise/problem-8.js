// Write a function that removes the last element from an array (pop).

// Approach 1: Using pop() Method
function removeValue(arr) {
  arr.pop(); // pop() মেথড ব্যবহার করে শেষ উপাদান সরানো হয়েছে
  return arr;
}

const array = [1, 2, 3];
console.log(removeValue(array));

// ---------------------------------------------------------------------------

// ব্যাখ্যা:
// 1. pop() সরাসরি অ্যারের শেষ উপাদানটি সরিয়ে দেয় এবং অ্যারের দৈর্ঘ্য স্বয়ংক্রিয়ভাবে কমিয়ে আনে।
// 2. এটি একটি in-place operation অর্থাৎ অ্যারের মেমরি একই থাকে।

// ---------------------------------------------------------------------------

// Approach 2: Manual Removal
function removeLastElementManual(arr) {
  if (arr.length === 0) {
    return "Array is already empty"; // যদি অ্যারে খালি থাকে
  }
  if (arr.length > 0) {
    arr.length = arr.length - 1; // অ্যারের দৈর্ঘ্য কমিয়ে শেষ উপাদান সরানো হয়েছে
  }
  return arr;
}

const nums = [4, 5, 6];
console.log(removeLastElementManual(nums));

// ---------------------------------------------------------------------------

// ব্যাখ্যা:
// 1. arr.length পরিবর্তন করে শেষ উপাদানটি সরানো হয়।
// 2. এটি অ্যারের মেমরি কাঠামোতে কোনো পরিবর্তন না এনে শেষ উপাদানটি মুছে ফেলে।

// ---------------------------------------------------------------------------

// Edge Case: Empty Array
// ফাঁকা অ্যারে থাকলে:
// - pop() মেথড: কিছুই সরানো হয় না এবং এটি কোনো ত্রুটি ছাড়াই কাজ করে।
// - ম্যানুয়াল পদ্ধতি: একইভাবে কোনো ত্রুটি ছাড়াই কাজ করবে।

// ---------------------------------------------------------------------------

// Time Complexity:
// - pop() মেথড: O(1), কারণ এটি সরাসরি শেষ উপাদান সরিয়ে ফেলে।
// - ম্যানুয়াল পদ্ধতি: O(1), কারণ length পরিবর্তন করাও একটি constant operation।

// ---------------------------------------------------------------------------

// Space Complexity:
// - উভয় পদ্ধতিতে অতিরিক্ত স্পেস ব্যবহার করা হয় না, তাই স্পেস কমপ্লেক্সিটি O(1)।
