// Write a function that adds a specific value to the beginning of an array (unshift).

// Approach 1: Using unshift() Method
function addValue(arr, num) {
  arr.unshift(num);
  return arr;
}

const array = [1, 2, 3];
console.log(addValue(array, 4));

// ---------------------------------------------------------------------------

// ব্যাখ্যা:
// - unshift() মেথড সরাসরি অ্যারের শুরুতে একটি মান যোগ করে এবং অন্য উপাদানগুলো স্বয়ংক্রিয়ভাবে এক ধাপ ডান দিকে সরিয়ে দেয়।

// ---------------------------------------------------------------------------

// Approach 2: Manually Adding Value by Shifting Elements
function addValueToBeg(arr, val) {
  for (let i = arr.length; i > 0; i--) {
    arr[i] = arr[i - 1];
  }
  arr[0] = val;
  return arr;
}

const nums = [4, 5, 6];
console.log(addValueToBeg(nums, 7));

// ---------------------------------------------------------------------------

// ব্যাখ্যা:
// - অ্যারের প্রতিটি উপাদান এক ধাপ করে ডান দিকে সরিয়ে প্রথম ইন্ডেক্সে নতুন মান যোগ করা হয়েছে।

// ---------------------------------------------------------------------------

// Edge Case: Empty Array
// - ফাঁকা অ্যারে থাকলেও উভয় পদ্ধতিতে নতুন মান সফলভাবে যোগ করা সম্ভব।

// ---------------------------------------------------------------------------

// Time Complexity:
// 1. unshift() মেথড: O(n), কারণ প্রতিটি উপাদানকে এক ধাপ ডান দিকে সরানোর প্রক্রিয়া স্বয়ংক্রিয়ভাবে ঘটে।
// 2. ম্যানুয়াল পদ্ধতি: O(n), কারণ আমরা একটি লুপ ব্যবহার করে প্রতিটি উপাদান সরাচ্ছি।

// ---------------------------------------------------------------------------

// Space Complexity:
// 1. উভয় পদ্ধতিতে অতিরিক্ত স্পেস ব্যবহার করা হয় না, তাই স্পেস কমপ্লেক্সিটি O(1)।
