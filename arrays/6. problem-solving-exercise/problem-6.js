// Write a function that adds a specific value to the end of an array (push).

// Approach 1: Using push() Method
function addValue(arr, num) {
  arr.push(num); // push() মেথড ব্যবহার করে মানটি অ্যারের শেষে যোগ করা হয়েছে
  return arr;
}

const array = [1, 2, 3];
console.log(addValue(array, 4));

// ---------------------------------------------------------------------------

// ব্যাখ্যা:
// 1. push() মেথড সরাসরি অ্যারের শেষে একটি মান যোগ করে।
// 2. এটি অ্যারের পরিবর্তিত ফর্ম রিটার্ন করে।

// ---------------------------------------------------------------------------

// Approach 2: Manually Adding Value Using Index
function addValueToEndManual(arr, val) {
  arr[arr.length] = val; // অ্যারের শেষ ইন্ডেক্সে মান যোগ করা হয়েছে
  return arr;
}

const num = [4, 5, 6];
console.log(addValueToEndManual(num, 7));

// ---------------------------------------------------------------------------

// ব্যাখ্যা:
// 1. অ্যারের length প্রপার্টি ব্যবহার করে নতুন ইন্ডেক্স নির্ধারণ করা হয়েছে।
// 2. সেই ইন্ডেক্সে নতুন মান অ্যাসাইন করা হয়েছে।

// ---------------------------------------------------------------------------

// Edge Case: Empty Array
// - উভয় পদ্ধতিতেই ফাঁকা অ্যারে থাকলেও নতুন মান যোগ করা সম্ভব।

// ---------------------------------------------------------------------------

// Time Complexity:
// - উভয় পদ্ধতির টাইম কমপ্লেক্সিটি O(1), কারণ নতুন মান যোগ করতে কোনো লুপ বা অতিরিক্ত প্রসেসিং প্রয়োজন হয় না।

// ---------------------------------------------------------------------------

// Space Complexity:
// - উভয় পদ্ধতির স্পেস কমপ্লেক্সিটি O(1), কারণ অতিরিক্ত কোনো স্পেস ব্যবহার করা হয় না।
