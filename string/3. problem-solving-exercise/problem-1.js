// Write a function concatStrings that takes two strings as input and returns a single concatenated string separated by a space.

// ----------------------------------------------------------------------------

function concatStrings(str1, str2) {
  return str1 + " " + str2;
}

const string1 = "Hello";
const string2 = "World";
console.log(concatStrings(string1, string2));

// ----------------------------------------------------------------------------

// ব্যাখ্যা:
// 1. ফাংশন ডিক্লারেশন:
//    concatStrings নামের একটি ফাংশন ডিক্লেয়ার করা হয়েছে, যা দুটি প্যারামিটার নেয়: str1 এবং str2।

// 2. কনক্যাটেনেশন প্রক্রিয়া:
//    - স্ট্রিং দুইটি একসাথে যোগ করা হয় প্লাস + অপারেটর ব্যবহার করে।
//    - str1 এবং str2 এর মধ্যে একটি স্পেস যোগ করা হয়েছে যাতে আউটপুটটি ভালোভাবে ফরম্যাট করা থাকে।

// 3. ফলাফল রিটার্ন করা:
//    - যোগ করা স্ট্রিংটি ফাংশন থেকে রিটার্ন করা হয়।

// 4. উদাহরণ:
//    - "Hello" এবং "World" স্ট্রিং একসাথে যোগ করলে আউটপুট হয় "Hello World"।
//    - "Bangladesh" এবং "Dhaka" যোগ করলে আউটপুট হয় "Bangladesh Dhaka"।

// ----------------------------------------------------------------------------

// কেন এই পদ্ধতি কার্যকর:
// - এটি সহজ এবং দ্রুত।
// - প্লাস + অপারেটর ব্যবহার করে স্ট্রিং কনক্যাটেনেশন করা জাভাস্ক্রিপ্টে একটি বেসিক এবং কার্যকরী পদ্ধতি।
