// স্ট্রিং উল্টো করার মানে হলো, স্ট্রিংয়ের অক্ষরগুলোর ক্রম পরিবর্তন করে শেষ থেকে শুরুতে সাজানো।
// এটি প্রোগ্রামিংয়ে খুব সাধারণ একটি সমস্যা, এবং এটি করার অনেক পদ্ধতি রয়েছে।

// ----------------------------------------------------------------------------

// কেন স্ট্রিং উল্টো করা শিখবেন?
// স্ট্রিং রিভার্সিং বিভিন্ন সমস্যা সমাধানে সাহায্য করে, যেমন:
// - পালিন্ড্রোম চেক করা।
// - ডেটা এনক্রিপশন।
// - রিভার্সিং ওয়ার্ড বা বাক্য।
// - বিভিন্ন অ্যালগরিদমিক সমস্যা সমাধান।

// ----------------------------------------------------------------------------

// স্ট্রিং উল্টো করার পদ্ধতি
// স্ট্রিং ইমিউটেবল (immutable), তাই এটি সরাসরি পরিবর্তন করা যায় না। নতুন একটি স্ট্রিং তৈরি করতে হয়। নিচে কয়েকটি পদ্ধতি দেখানো হলো:

// ----------------------------------------------------------------------------

// ১. অ্যারে মেথড ব্যবহার করে

// ধাপগুলো:
// 1. স্ট্রিংকে একটি অ্যারেতে ভেঙে নিন (split() মেথড ব্যবহার করে)।
// 2. অ্যারেটি উল্টো করুন (reverse() মেথড ব্যবহার করে)।
// 3. পুনরায় অ্যারেটিকে স্ট্রিং-এ রূপান্তর করুন (join() মেথড ব্যবহার করে)।

// কোড:

function reverseString(str) {
  return str.split("").reverse().join("");
}

console.log(reverseString("hello"));
// Output: "olleh"

console.log(reverseString("Shipan Mallik"));
// Output: "killaM napihS"

// ----------------------------------------------------------------------------

// ২. লুপ ব্যবহার করে (ম্যানুয়াল পদ্ধতি)

// ধাপগুলো:
// 1. নতুন একটি খালি স্ট্রিং তৈরি করুন।
// 2. স্ট্রিংয়ের শেষ অক্ষর থেকে শুরু করে একটি একটি করে নতুন স্ট্রিংয়ে যোগ করুন।

// কোড:

function reverseString1(str) {
  let reversed = "";
  for (let i = str.length - 1; i >= 0; i--) {
    reversed += str[i];
  }
  return reversed;
}

console.log(reverseString1("hello"));
// Output: "olleh"

console.log(reverseString1("Bangladesh"));
// Output: "hsedalgnaB"

// ----------------------------------------------------------------------------

// ৩. রেকার্সন ব্যবহার করে

// ধাপগুলো:
// 1. বেস কেস নির্ধারণ করুন: যদি স্ট্রিং খালি বা এক অক্ষরের হয়, সেটি রিটার্ন করুন।
// 2. প্রথম অক্ষর বাদ দিয়ে বাকি অংশের জন্য ফাংশন কল করুন।
// 3. প্রথম অক্ষরটি শেষে যোগ করুন।

// কোড:

function reverseString2(str) {
  if (str === "") return "";
  return reverseString2(str.slice(1)) + str[0];
}

console.log(reverseString2("hello"));
// Output: "olleh"

console.log(reverseString2("JavaScript"));
// Output: "tpircSavaJ"

// ----------------------------------------------------------------------------

// ৪. ES6 স্প্রেড অপারেটর ব্যবহার করে

// ধাপগুলো:
// 1. স্ট্রিংকে স্প্রেড অপারেটরের মাধ্যমে অক্ষরের অ্যারেতে রূপান্তর করুন।
// 2. অ্যারেটি উল্টো করুন এবং পুনরায় স্ট্রিং তৈরি করুন।

// কোড:

function reverseString3(str) {
  return [...str].reverse().join("");
}

console.log(reverseString3("hello"));
// Output: "olleh"

console.log(reverseString3("Shipan Mallik"));
// Output: "killaM napihS"

