// স্ট্রিং প্যাডিং হলো একটি নির্দিষ্ট দৈর্ঘ্যে পৌঁছানোর জন্য স্ট্রিংয়ের শুরুতে বা শেষে অতিরিক্ত অক্ষর যোগ করা। 
// JavaScript-এ স্ট্রিং প্যাডিং করার জন্য দুটি মেথড রয়েছে:

// 1. padStart(): স্ট্রিংয়ের শুরুতে নির্দিষ্ট সংখ্যা বা অক্ষর যোগ করে।
// 2. padEnd(): স্ট্রিংয়ের শেষে নির্দিষ্ট সংখ্যা বা অক্ষর যোগ করে।

// ----------------------------------------------------------------------------

// ১. padStart()
// padStart() মেথড একটি স্ট্রিংয়ের শুরুতে অতিরিক্ত অক্ষর যোগ করে নির্দিষ্ট দৈর্ঘ্য তৈরি করে।

// সিনট্যাক্স:
str.padStart(targetLength, padString);

// - targetLength: যে দৈর্ঘ্যের স্ট্রিং প্রয়োজন।
// - padString: যে অক্ষর স্ট্রিংয়ে যোগ করা হবে (ডিফল্ট: একটি স্পেস)।

// উদাহরণ:
const str = "5";
console.log(str.padStart(4, "0")); 
// Output: "0005" (মোট দৈর্ঘ্য ৪ এবং সামনে ০ যোগ করা হয়েছে)

console.log("123".padStart(6, "*")); 
// Output: "***123" (মোট দৈর্ঘ্য ৬ এবং সামনে * যোগ করা হয়েছে)

// ----------------------------------------------------------------------------

// ২. padEnd()
// padEnd() মেথড একটি স্ট্রিংয়ের শেষে অতিরিক্ত অক্ষর যোগ করে নির্দিষ্ট দৈর্ঘ্য তৈরি করে।

// সিনট্যাক্স:
str.padEnd(targetLength, padString);

// - targetLength: যে দৈর্ঘ্যের স্ট্রিং প্রয়োজন।
// - padString: যে অক্ষর স্ট্রিংয়ে যোগ করা হবে (ডিফল্ট: একটি স্পেস)।

উদাহরণ:
const str1 = "5";
console.log(str1.padEnd(4, "0")); 
// Output: "5000" (মোট দৈর্ঘ্য ৪ এবং শেষে ০ যোগ করা হয়েছে)

console.log("123".padEnd(6, "!")); 
// Output: "123!!!" (মোট দৈর্ঘ্য ৬ এবং শেষে ! যোগ করা হয়েছে)

// ----------------------------------------------------------------------------

// প্যাডিং-এর ব্যবহার
// স্ট্রিং প্যাডিং সাধারণত ফরম্যাটিং এবং ডাটা প্রেজেন্টেশনে ব্যবহৃত হয়, যেমন:

// 1. সংখ্যা ফরম্যাটিং:
const invoiceNumber = "123";
console.log(invoiceNumber.padStart(10, "0")); 
// Output: "0000000123" (চালানের নম্বর ফরম্যাট করা)


// 2. টেবিল ডাটা প্রেজেন্টেশন:
const name = "Shipan";
console.log(name.padEnd(15) + "| Score: 95"); 
// Output: "Shipan         | Score: 95"

// ----------------------------------------------------------------------------

// যদি টার্গেট দৈর্ঘ্য স্ট্রিংয়ের চেয়ে ছোট হয়?
// যদি স্ট্রিং ইতোমধ্যেই নির্ধারিত দৈর্ঘ্যের সমান বা বড় হয়, তাহলে প্যাডিং কোনো পরিবর্তন করবে না।

console.log("Hello".padStart(3, "*")); 
// Output: "Hello" (কারণ স্ট্রিংয়ের দৈর্ঘ্য ৫, যা টার্গেট দৈর্ঘ্যের চেয়ে বেশি)
