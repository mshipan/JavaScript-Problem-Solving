// স্ট্রিং থেকে নির্দিষ্ট অক্ষর বা অক্ষরের একটি সেট সরানোর প্রয়োজন অনেক সময় হয়। JavaScript-এ এটি
// করার জন্য বিভিন্ন পদ্ধতি বা মেথড ব্যবহার করা যায়। নিচে একাধিক উপায় নিয়ে আলোচনা করা হলো:

// ----------------------------------------------------------------------------

// ১. replace() মেথড ব্যবহার করে
// replace() মেথড একটি স্ট্রিং থেকে নির্দিষ্ট অক্ষর বা প্যাটার্ন সরাতে ব্যবহৃত হয়। এটি কেস-সেনসিটিভ।

// উদাহরণ:
const str = "hello world!";
const result = str.replace("!", "");
console.log(result);
// Output: "hello world" (বিস্ময়চিহ্ন সরানো হয়েছে)

// সকল মিলে যাওয়া অক্ষর সরানোর জন্য (g ফ্ল্যাগ ব্যবহার করে):
const str1 = "hello world!";
const result1 = str1.replace(/o/g, "");
console.log(result1);
// Output: "hell wrld" (সবগুলো 'o' সরানো হয়েছে)

// ----------------------------------------------------------------------------

// ২. split() এবং join() মেথড ব্যবহার করে
// split() মেথড দিয়ে স্ট্রিংকে একটি অ্যারের মধ্যে ভাগ করা যায় এবং join() মেথড ব্যবহার করে সেটি আবার একটি নতুন স্ট্রিংয়ে পরিণত করা যায়।

// উদাহরণ:
const str2 = "hello world!";
const result2 = str2.split("o").join("");
console.log(result2);
// Output: "hell wrld" (সবগুলো 'o' সরানো হয়েছে)

// ----------------------------------------------------------------------------

// ৩. filter() মেথড ব্যবহার করে (অ্যারের মতো প্রক্রিয়াকরণ)
// স্ট্রিংকে একটি অ্যারেতে রূপান্তর করে ফিল্টার করা যেতে পারে।

// উদাহরণ:
const str3 = "hello world!";
const result3 = [...str3].filter((char) => char !== "o").join("");
console.log(result3);
// Output: "hell wrld" (সবগুলো 'o' সরানো হয়েছে)

// ----------------------------------------------------------------------------

// ৪. replaceAll() মেথড ব্যবহার করে (ES2021 বা পরবর্তীতে)
// replaceAll() মেথড সরাসরি স্ট্রিংয়ের মধ্যে সব মিলে যাওয়া অক্ষর সরাতে ব্যবহৃত হয়।

// উদাহরণ:
const str4 = "hello world!";
const result4 = str4.replaceAll("o", "");
console.log(result4);
// Output: "hell wrld" (সবগুলো 'o' সরানো হয়েছে)

// ----------------------------------------------------------------------------

// ৫. নির্দিষ্ট অক্ষরের সেট সরানোর জন্য RegExp ব্যবহার
// নিয়মিত অভিব্যক্তি (Regular Expression) দিয়ে স্ট্রিং থেকে একাধিক নির্দিষ্ট অক্ষর সরানো যায়।

// উদাহরণ:
const str5 = "hello world!";
const result5 = str5.replace(/[!o]/g, "");
console.log(result5);
// Output: "hell wrld" (বিস্ময়চিহ্ন এবং সব 'o' সরানো হয়েছে)

// ----------------------------------------------------------------------------

// ৬. শুধুমাত্র অক্ষর রেখে বাকিগুলো সরানো
// যদি স্ট্রিং থেকে শুধুমাত্র অক্ষর রেখে অন্যসব সরাতে চান, তাহলে Regular Expression ব্যবহার করতে পারেন।

// উদাহরণ:
const str6 = "hello world 123!";
const result6 = str6.replace(/[^a-zA-Z]/g, "");
console.log(result6);
// Output: "helloworld" (শুধুমাত্র অক্ষর রাখা হয়েছে)
