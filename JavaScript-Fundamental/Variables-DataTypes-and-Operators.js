// There are three types of Variables
// They are var, let and const

// ****** variable declaration ******
// var
var name = "Shipan Mallik";
var my_name = "Shipan Mallik";
var myName = "Shipan Mallik";
// these types of varialbes are valid

// let
let persom = "Shipan";

// const
const someOne = "Shipan Mallik";

// "var" is global scope, "let" is global and block both scope and "const" is only block scope

// ****** Data Types ******

