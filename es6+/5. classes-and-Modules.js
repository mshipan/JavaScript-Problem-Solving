// 1. জাভাস্ক্রিপ্ট ক্লাস (Classes) - ES6+
//    ক্লাস হলো জাভাস্ক্রিপ্টের একটি ব্লুপ্রিন্ট বা টেম্পলেট যা অবজেক্ট তৈরি করতে ব্যবহৃত হয়। এটি ES6 থেকে চালু হয়েছে এবং এর মাধ্যমে অবজেক্ট-ওরিয়েন্টেড প্রোগ্রামিং (OOP)
//    সহজে প্রয়োগ করা যায়। ক্লাসের মাধ্যমে আপনি একই ধরনের অবজেক্টের জন্য সাধারণ প্রোপার্টি এবং মেথড তৈরি করতে পারেন।

//    উদাহরণ:

class Person {
  constructor(name, age) {
    this.name = name;
    this.age = age;
  }

  greet() {
    console.log(
      `Hello, my name is ${this.name} and I am ${this.age} years old.`
    );
  }
}

// ক্লাস থেকে একটি অবজেক্ট তৈরি করা
const person1 = new Person("Shipan", 25);
person1.greet(); // Output: Hello, my name is Shipan and I am 25 years old.

// ব্যাখ্যা:
// - class Person: এটি একটি ক্লাস ডিক্লারেশন। Person হলো ক্লাসের নাম।
// - constructor(name, age): এটি একটি বিশেষ মেথড, যা নতুন অবজেক্ট তৈরি করার সময় চালু হয়। এটি ইনিশিয়াল ভ্যালু সেট করতে ব্যবহৃত হয়।
// - this.name = name; এবং this.age = age;: ক্লাসের প্রোপার্টিগুলোকে ইনিশিয়ালাইজ করা হয়।
// - greet() মেথড: এটি ক্লাসের একটি মেথড, যা প্রিন্ট করে ক্লাসের প্রোপার্টি অ্যাক্সেস করে।

// 2. মডিউলস (Modules) - ES6+
//    মডিউল হলো জাভাস্ক্রিপ্ট কোডের একটি পৃথক ইউনিট যা অন্য মডিউলের কোডের সঙ্গে সম্পর্কহীনভাবে ব্যবহার করা যায়। মডিউলস আপনাকে কোডকে
//    ছোট ছোট ভাগে বিভক্ত করতে এবং বিভিন্ন মডিউলকে একত্রে যুক্ত করতে সাহায্য করে। ES6 থেকে জাভাস্ক্রিপ্টে import এবং export কীওয়ার্ডের মাধ্যমে মডিউল ব্যবহারের সুবিধা চালু হয়েছে।

//    উদাহরণ:
//    মডিউল ফাইল (mathModule.js):

// mathModule.js
export function add(a, b) {
  return a + b;
}

export function subtract(a, b) {
  return b - a;
}

//    মডিউল ইম্পোর্ট করা (main.js):

// main.js
import { add, subtract } from "./mathModule.js";

console.log(add(5, 3)); // Output: 8
console.log(subtract(2, 7)); // Output: 5

// ব্যাখ্যা:
// - export: মডিউল থেকে নির্দিষ্ট ফাংশন, ভেরিয়েবল, অথবা ক্লাস অন্য ফাইল থেকে ব্যবহার করার জন্য export করা হয়।
// - import: অন্য ফাইল থেকে এক বা একাধিক export করা ফাংশন, ভেরিয়েবল, অথবা ক্লাসকে import করা হয়।
